/**Operadores de Comparação
> Maior
>= Maior que ou igual a 
< menor
<= menor que ou igual a
== igualdade
=== igualdade estrita (valor e tipo)
!= diferente
!== diferente estrita (valor e tipo)
*/
console.log(10 > 5);    //true
console.log(10 >= 5);   //true
console.log(10 < 5);    //false
console.log(10 <= 5);   //false
console.log(10 === 5);  //false
console.log(10 == 5);   //false
console.log(10 != 5);   //true
console.log(10 !== 5);  //true